package com.insurance.Insurance_spring.controller;

public class AcceptForm {
    private String accidentDate, accidentTime, accidentPlace, accidentType, accidentSize;

    public String getAccidentDate() {
        return accidentDate;
    }

    public void setAccidentDate(String accidentDate) {
        this.accidentDate = accidentDate;
    }

    public String getAccidentTime() {
        return accidentTime;
    }

    public void setAccidentTime(String accidentTime) {
        this.accidentTime = accidentTime;
    }

    public String getAccidentPlace() {
        return accidentPlace;
    }

    public void setAccidentPlace(String accidentPlace) {
        this.accidentPlace = accidentPlace;
    }

    public String getAccidentType() {
        return accidentType;
    }

    public void setAccidentType(String accidentType) {
        this.accidentType = accidentType;
    }

    public String getAccidentSize() {
        return accidentSize;
    }

    public void setAccidentSize(String accidentSize) {
        this.accidentSize = accidentSize;
    }
}
